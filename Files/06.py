#6- Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

print('--- Área do Circulo ---')

raio = int(input('Por favor, digite o raio do círculo: '))

area = 3.14 * (raio * raio)

print(' --------------------------------------- ')

print(f'A área do círculo é: {area}')

